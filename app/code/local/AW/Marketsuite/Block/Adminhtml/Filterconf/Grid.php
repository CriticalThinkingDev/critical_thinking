class AW_Marketsuite_Block_Adminhtml_Filterconf_Grid extends Mage_Adminhtml_Block_Widget_Grid
{

    public function __construct()
    {
        parent::__construct();
        $this->setId('filter_id');
        $this->setDefaultSort('name');
        $this->setDefaultDir('ASC');
        $this->setSaveParametersInSession(true);
    }

    protected function _prepareCollection()
    {
        $collection = Mage::getModel('marketsuite/filter')
            ->getResourceCollection();
        $this->setCollection($collection);
        return parent::_prepareCollection();
    }

    protected function _prepareColumns()
    {
        $this->addColumn('filter_id', array(
            'header'    => Mage::helper('marketsuite')->__('ID'),
            'align'     =>'right',
            'width'     => '50px',
            'index'     => 'filter_id',
        ));

        $this->addColumn('name', array(
            'header'    => Mage::helper('marketsuite')->__('Rule Name'),
            'align'     =>'left',
            'index'     => 'name',
        ));

        $this->addColumn('is_active', array(
            'header'    => Mage::helper('marketsuite')->__('Status'),
            'align'     =>'left',
            'width'     => '100px',
            'index'     => 'is_active',
            'type'      => 'options',
            'options'   => array(
                1 => 'Active',
                0 => 'Inactive',
            ),
        ));

        return parent::_prepareColumns();
    }

    public function getRowUrl($row)
    {
        return $this->getUrl('*/*/edit', array('id' => $row->getFilterId()));
    }

}
